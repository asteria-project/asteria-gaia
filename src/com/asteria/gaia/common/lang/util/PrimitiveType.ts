/**
 * The <code>PrimitiveType</code> enum contains values that represent JavaScript primitive types.
 */
export enum PrimitiveType {

    /**
     * Represents the <code>string</code> primitive type.
     */
    STRING = 'string',
    
    /**
     * Represents the <code>number</code> primitive type.
     */
    NUMBER = 'number',
    
    /**
     * Represents the <code>array</code> primitive type.
     */
    ARRAY = 'array',
    
    /**
     * Represents the <code>object</code> primitive type.
     */
    OBJECT = 'object'
}
